//JavaScript Operators

//Baisc Operators
let indox = 10;
//Addition
console.log(indox + 5);
//Subtraction
console.log(indox - 5);
//Multiplication
console.log(indox * 5);
//Division
console.log(indox / 5);
//Power
console.log(indox ** 5);
//Remainder
console.log(indox % 5);

//Add 1
indox++;
console.log(indox);

//Add 30
indox += 30;
console.log(indox);

//Subtract 1
indox--;
console.log(indox);

//Subtract 30
indox -= 30;
console.log(indox);
